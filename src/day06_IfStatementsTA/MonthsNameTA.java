package day06_IfStatementsTA;

public class MonthsNameTA {
    public static void main(String[] args) {

        int number = 3;
        String result = "";
        if (number == 1) {
            result = "january";
        }
        if (number == 2) {
            result = "february";
        }
        if (number == 3) {
            result = "march";
        }
        if (number == 4) {
            result = "april";
        }
        if (number == 5) {
            result = "may";
        }
        if (number == 6) {
            result = "june";
        }
        if (number == 7) {
            result = "july";
        }
        if (number == 8) {
            result = "august";
        }
        if (number == 9) {
            result = "september";
        }
        if (number == 10) {
            result = "october";
        }
        if (number == 11) {
            result = "november";
        }
        if (number == 12) {
            result = "december";
        }
        System.out.println(result);
    }
}
