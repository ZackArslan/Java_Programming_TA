package day16_nestedLoop;

public class nestedLoopIntro {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("hello");
        }
        for (int i = 0; i < 5; i++) {
            System.out.println("hello");
        }for (int i = 0; i < 5; i++) {
            System.out.println("hello");
        }for (int i = 0; i < 5; i++) {
            System.out.println("hello");
        }

        System.out.println("----------");

        for (int i = 0; i < 5; i++) { //i=0

            for (int j = 0; j < 5; j++) {
                System.out.println("hello");
            }

        }





    }
}
