package day02_escapeSequencesTa;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("hello everyone\nToday we have java class");
        System.out.println("...............");
        System.out.println("\tJava programming language");
        System.out.println("\t\tjava developer");
        System.out.println("...............");
        System.out.println("\\");
        System.out.println("...............");
        System.out.println("my favorite tv show is \"kizilcik serbeti\"");

    }

    /*
    1. \n new line
    2. \t paragraph space
    3. \\ Back slash
    4. \" double quote
     */















}
